#include "ftprintf.h"

size_t	ft_llintlen(long long int a)
{
	size_t	i;
	
	if (a == 0)
		return (1);
	i = 0;
	if (a < 0)
	{
		a *= -1;
		i++;
	}
	while (a)
	{
		a = a / 10;
		i++;
	}
	return (i);
}
